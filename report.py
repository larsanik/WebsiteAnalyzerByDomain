def generate_report(data: dict) -> None:
    """
    Принимает словарь с обработанными данными домена
    и выводит структурированный отчет в терминал.
    """
    if not data:
        print("[ОШИБКА] Нет данных для формирования отчета.")
        return

    # Определяем заголовок отчета
    domain_name = data.get("Доменное имя", "Неизвестный домен").upper()
    header = f" АНАЛИЗ ДОМЕНА: {domain_name} "

    # Печатаем красивую шапку отчета
    print("\n" + "=" * 60)
    print(header.center(60, "="))
    print("=" * 60)

    # Выводим каждое поле в виде аккуратного списка с выравниванием
    for key, value in data.items():
        # Если значение слишком длинное (например, список DNS-серверов или статусов),
        # мы можем сделать перенос строки для сохранения читаемости
        if len(str(value)) > 40 and ", " in str(value):
            # Форматируем списки, заменяя запятые на переносы с отступами
            formatted_value = "\n    " + str(value).replace(", ", ",\n    ")
            print(f"• {key}: {formatted_value}")
        else:
            print(f"• {key}: {value}")

    # Печатаем подвал отчета
    print("=" * 60 + "\n")


